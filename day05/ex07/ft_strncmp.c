int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;
	int diff;

	i = 1;
	while (*s1 == *s2 && i < (int)n && *s1 != '\0')
	{
		s1++;
		s2++;
		i++;
	}
	diff = *s1 - *s2;
	return (diff);
}
